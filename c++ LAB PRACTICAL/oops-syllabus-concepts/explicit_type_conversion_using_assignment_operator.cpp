#include <iostream>  
using namespace std;  
int main ()  
{  
// declare a float variable  
float num2;  
// initialize an int variable  
int num1 = 25;  
  
// convert data type from int to float  
num2 = (float) num1;   // <--
cout << " The value of int num1 is: " << num1 << endl;  
cout << " The value of float num2 is: " << num2 << endl;  
return 0;  
}  